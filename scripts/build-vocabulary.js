#!/usr/bin/env node
/**
 * Builds js/vocabulary-data.js from data/source/vocabulary.csv
 * Run: npm run build
 */

const fs = require("fs");
const path = require("path");
const { parseKellyRows, KELLY_FILE } = require("./kelly-vocabulary");
const {
  buildVocabularyData,
  parseUnifiedCsv,
  setGlobalExtras,
  collectAllExtras,
  buildExtrasEnMap,
  mergeKellyMetadata,
  applyMostCommonFlags,
  applySentenceEnrichmentCache,
} = require("./vocabulary-lib");

const SOURCE_DIR = path.join(__dirname, "../data/source");
const VOCABULARY_CSV = path.join(SOURCE_DIR, "vocabulary.csv");
const SENTENCE_CACHE = path.join(__dirname, "../data/sentence-enrichment-cache.json");
const OUTPUT = path.join(__dirname, "../js/vocabulary-data.js");

function loadGlobalExtras() {
  const extrasSources = [];
  for (const name of [
    "Swedish Vocabulary Practice - Adjetivos.csv",
    "Swedish Vocabulary Practice - Advérbios.csv",
    "Swedish Vocabulary Practice - Verbos.csv",
  ]) {
    const filePath = path.join(SOURCE_DIR, name);
    if (fs.existsSync(filePath)) {
      extrasSources.push(fs.readFileSync(filePath, "utf8"));
    }
  }

  if (extrasSources.length) {
    const ptMap = collectAllExtras(extrasSources);
    setGlobalExtras(ptMap, buildExtrasEnMap(ptMap));
  }
}

function main() {
  if (!fs.existsSync(VOCABULARY_CSV)) {
    console.error(
      `Missing ${VOCABULARY_CSV}\nRun: npm run build:migrate`
    );
    process.exit(1);
  }

  loadGlobalExtras();

  const content = fs.readFileSync(VOCABULARY_CSV, "utf8");
  const entries = parseUnifiedCsv(content);

  const enriched = applySentenceEnrichmentCache(entries, SENTENCE_CACHE);
  if (enriched.sentences) {
    console.log(
      `  Sentence cache: ${enriched.sentences} sentence translation(s), ${enriched.tokens} contextual token gloss(es)`
    );
  }

  const kellyPath = path.join(SOURCE_DIR, KELLY_FILE);
  if (fs.existsSync(kellyPath)) {
    const kellyItems = parseKellyRows(fs.readFileSync(kellyPath, "utf8"));
    mergeKellyMetadata(entries, kellyItems);
  }
  applyMostCommonFlags(entries);

  const practice = entries.filter((e) => e.source !== "kelly").length;
  const kelly = entries.filter((e) => e.source === "kelly").length;
  const mostCommon = entries.filter((e) => e.mostCommon).length;

  const data = buildVocabularyData(entries, {
    practice,
    kelly,
    source: "vocabulary.csv",
  });

  const js = `/* Auto-generated by scripts/build-vocabulary.js — do not edit */\nwindow.VOCABULARY_DATA = ${JSON.stringify(data, null, 2)};\n`;
  fs.writeFileSync(OUTPUT, js);

  console.log(`Wrote ${entries.length} entries to ${OUTPUT}`);
  console.log(`  Practice: ${practice}`);
  console.log(`  Kelly: ${kelly}`);
  console.log(`  Most common (top 250/type): ${mostCommon}`);
}

main();
